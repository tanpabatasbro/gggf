#operator aritmatika

angka_1 = int(input("masukkan angka pertama :"))
angka_2 = int(input("masukkan angka kedua :"))
#penjumlahan
hasil = angka_1 + angka_2
print("{} + {} = {}". format(angka_1,angka_2,hasil))
#pengurangan
hasil = angka_1 - angka_2
print("{} - {} = {}". format(angka_1,angka_2,hasil))
#perkalian
hasil = angka_1 * angka_2
print("{} * {} = {}". format(angka_1,angka_2,hasil))
#pembagian
hasil = angka_1 / angka_2
print("{} / {} = {}". format(angka_1,angka_2,hasil))
#sisa bagi
hasil = angka_1 % angka_2
print("{} % {} = {}". format(angka_1,angka_2,hasil))
#pangkat
hasil = angka_1 ** angka_2
print("{} ** {} = {}". format(angka_1,angka_2,hasil))
#pembagian bulat
hasil = angka_1 // angka_2
print("{} // {} = {}". format(angka_1,angka_2,hasil))
